// post increment i++
// pre increment ++i

// for state has 3 components 
/**
 * 1 is variable declaration
 * 2 condition
 * 3 increment or decrement
 */

for (let i = 1; i <= 5; i++){
    if (i % 2 == 0){
        console.log("Even: ", i)
    }

    if (i % 2 != 0){
        console.log("Odd: ", i)
    }
}